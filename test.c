#include <stdio.h>

int main () {
    //Adding the addition feature
    int a = 7;
    int b = 9; 
    int c = a + b;
    printf("Hello World!!!\n");
    printf("%d + %d = %d\n", a, b, c);
    //Adding the subtraction feature
    int d = 10; 
    int e = 5; 
    int f = d - e;
    printf("%d - %d = %d\n", d, e,f);
    int g = 12; 
    int h = 12; 
    int i = g * h;
    printf("%d * %d = %d\n", g, h, i);
    int j = 12; 
    int k = 4; 
    int l = 12/4; 
    printf("%d / %d = %d\n", j, k, l);
    printf("Done Done DONE!!!\n");
    return 0;
}
